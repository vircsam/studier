import { db } from "../firebase/config";
import { 
  doc, 
  setDoc, 
  getDoc, 
  updateDoc, 
  deleteDoc, 
  collection
} from "firebase/firestore";

function sanitizeForFirestore(value) {
  if (value === undefined) {
    return null;
  }

  if (value === null || typeof value === "string" || typeof value === "number" || typeof value === "boolean") {
    return value;
  }

  if (Array.isArray(value)) {
    return value
      .map((item) => sanitizeForFirestore(item))
      .filter((item) => item !== undefined);
  }

  if (value instanceof Date) {
    return value.toISOString();
  }

  if (typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value)
        .map(([key, nestedValue]) => [key, sanitizeForFirestore(nestedValue)])
        .filter(([, nestedValue]) => nestedValue !== undefined)
    );
  }

  return String(value);
}

/**
 * User Profile Services
 */
export async function createUserProfile(uid, profileData) {
  const userRef = doc(db, "users", uid);
  const userSnap = await getDoc(userRef);
  
  const userData = {
    uid,
    name: profileData.displayName || profileData.name || profileData.email.split("@")[0],
    displayName: profileData.displayName || profileData.name || profileData.email.split("@")[0],
    email: profileData.email,
    photoURL: profileData.photoURL || null,
    lastLogin: new Date().toISOString(),
    lastActiveDate: new Date().toISOString().split("T")[0],
  };

  if (!userSnap.exists()) {
    userData.createdAt = new Date().toISOString();
    userData.streak = 1;
    userData.productivityScore = 60;
    await setDoc(userRef, userData);
    return userData;
  } else {
    const existing = userSnap.data();
    // Streak calculations
    const today = new Date().toISOString().split("T")[0];
    const yesterday = new Date(Date.now() - 86400000).toISOString().split("T")[0];
    let streak = existing.streak || 1;

    if (existing.lastActiveDate === yesterday) {
      streak += 1;
    } else if (existing.lastActiveDate !== today) {
      streak = 1;
    }

    const updatedData = {
      ...existing,
      ...userData,
      streak,
      lastActiveDate: today
    };

    await updateDoc(userRef, {
      name: updatedData.name,
      displayName: updatedData.displayName,
      photoURL: updatedData.photoURL,
      lastLogin: updatedData.lastLogin,
      lastActiveDate: updatedData.lastActiveDate,
      streak: updatedData.streak
    });

    return updatedData;
  }
}

export async function getUserProfile(uid) {
  const userSnap = await getDoc(doc(db, "users", uid));
  return userSnap.exists() ? userSnap.data() : null;
}

/**
 * Flashcard Services
 */
export async function dbAddFlashcard(card) {
  // Let the document ID be generated by Firestore or passed
  const docId = card.id || doc(collection(db, "flashcards")).id;
  const cardData = {
    ...card,
    id: docId,
    createdAt: card.createdAt || new Date().toISOString(),
    updatedAt: new Date().toISOString()
  };
  await setDoc(doc(db, "flashcards", docId), sanitizeForFirestore(cardData));
  return cardData;
}

export async function dbUpdateFlashcard(id, updates) {
  const cardRef = doc(db, "flashcards", id);
  const cleanUpdates = {
    ...updates,
    updatedAt: new Date().toISOString()
  };
  await updateDoc(cardRef, sanitizeForFirestore(cleanUpdates));
}

export async function dbDeleteFlashcard(id) {
  await deleteDoc(doc(db, "flashcards", id));
}

